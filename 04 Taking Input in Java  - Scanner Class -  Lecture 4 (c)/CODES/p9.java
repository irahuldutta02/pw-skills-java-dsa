public class p9
{
    public static void main(String[] args) {
        int length=6;
        int breadth=5;
        System.out.println("The length is : "+ length);
        System.out.println("The breadth is : "+ breadth);
        int area=length*breadth;
        System.out.println("The area is : "+area);
    }
}